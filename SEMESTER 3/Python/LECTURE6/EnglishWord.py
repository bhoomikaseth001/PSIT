num=int(input("Enter a single digit number : "))
if num==1:
    print("ONE")
elif num==2:
    print("TWO")
elif num==3:
    print("THREE")
elif num==4:
    print("FOUR")
elif num==5:
    print("FIVE")
elif num==6:
    print("SIX")
elif num==7:
    print("SEVEN")
elif num==8:
    print("EIGHT")
elif num==9:
    print("NINE")
elif num==0:
    print("ZERO")
else:
    print("Enter a single digit number")  
